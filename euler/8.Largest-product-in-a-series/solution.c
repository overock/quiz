#include <stdio.h>

#define NARRAY(arr) (sizeof(arr) / sizeof(arr[0]))

int mul(int* arr, int n)
{
  int product = 1, i;
  for (i=0; i<n; ++i) {
    product *= arr[i];
  }
  return product;
}

int max(int a, int b)
{
  return (b < a) ? a : b;
}

